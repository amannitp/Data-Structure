import java.util.*;
public class Operator {
    public static void main(String[] args) {
        int x=10;
        int y=49;
        int z=21;
        System.out.println(x++);
        System.out.println(++y);
        System.out.println(z--);
        System.out.println(x);
        System.out.println(z);
        
    }
    
}
