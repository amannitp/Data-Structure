import java.util.*;
public class RelationalOperator {
    public static void main(String[] args) {

        System.out.println(~4);
        System.out.println(~8);
        System.out.println(~-8);
        System.out.println(~25);
        System.out.println(~-25);
        System.out.println(~-2567);
    }
    
}
